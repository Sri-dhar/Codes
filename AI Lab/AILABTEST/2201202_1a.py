a = int(input("Enter a number : "))
b = int(input("Enter the second number : "))

operation = input("Enter the operator  ('+','-','*' or '/') : ")

if(operation == '+'):
	print(f"The output is : "+str( a+b))
if(operation == '-'):
	print(f"The output is : "+ str(a-b))
if(operation == '*'):
	print(f"The output is : "+ str(a*b))
if(operation == '/'):
	print(f"The output is : "+ str(a/b))




